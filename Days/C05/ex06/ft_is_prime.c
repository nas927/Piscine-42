int		ft_is_prime(int nb)
{
	int		i;

	i = 2;
	if (nb < i)
		return (0);
	else
	{
		while (i <= (nb / i)) // Don't forget...
		{
			if(nb % i == 0)
				return (0);
			i++;
		}
	}
	return (1);
}

int		main(void)
{
	printf("%d", ft_is_prime(10));
}
