int		ft_sqrt(int nb)
{
	int		i;

	i = 0;
	if (nb <= 0)
		return (0);
	while (i * i < nb && i <= 46341) // Don't forget...
		i++;
	if (i * i == nb)
		return (i);
	return (0);
}

int		main(void)
{
	printf("%d", ft_sqrt(214));
}
